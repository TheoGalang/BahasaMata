package com.example.bahasamata.model;

public class Alarm {
    String id,id_difabel,id_perawat,title,jam;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getId_difabel() {
        return id_difabel;
    }

    public void setId_difabel(String id_difabel) {
        this.id_difabel = id_difabel;
    }

    public String getId_perawat() {
        return id_perawat;
    }

    public void setId_perawat(String id_perawat) {
        this.id_perawat = id_perawat;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getJam() {
        return jam;
    }

    public void setJam(String jam) {
        this.jam = jam;
    }
}
